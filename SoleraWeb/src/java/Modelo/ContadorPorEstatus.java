package Modelo;

public class ContadorPorEstatus {
    private String terminados;
    private String seguimiento;
    private String incorrectos;

    public String getTerminados() {
        return this.terminados;
    }

    public void setTerminados(String terminados) {
        this.terminados = terminados;
    }

    public String getSeguimiento() {
        return this.seguimiento;
    }

    public void setSeguimiento(String seguimiento) {
        this.seguimiento = seguimiento;
    }

    public String getIncorrectos() {
        return this.incorrectos;
    }

    public void setIncorrectos(String incorrectos) {
        this.incorrectos = incorrectos;
    }

}
